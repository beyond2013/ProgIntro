#include <stdio.h>

/*
 * This program displays a table of escape sequence
 * using escape sequences.
*/

int main(int argc, char *argv[])
{
  printf("\n=============================================");  	
  printf("\n Escape Sequence  | \t Description \n");  	
  printf("=============================================\n");  	
  printf(" \\n \t\t  | \t New line \n");  	
  printf(" \\r \t\t  | \t Carriage return \n");  	
  printf(" \\t \t\t  | \t tab \n");  	
  printf("=============================================\n");  	
	return 0;
}
